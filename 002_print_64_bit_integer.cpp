#include <iostream>
#include <bitset>

using namespace std;

int main(int argc, char *argv[])
{
    unsigned long long int x = 0xFFFFFFFFFFFFFFFF;
    long v = 0x12345678;
    long minus_v = -0x12345678;
    std::cout << std::bitset<64>(x) << std::endl;
    std::cout << std::bitset<64>(v) << std::endl;
    std::cout << std::bitset<64>(minus_v) << std::endl;
    return 0;
}

